"""
OAT (One-At-a-Time) Sensitivity Analysis
=========================================
Analyzes model outputs from OAT parameter sweeps generated by:
  python sensitivity_analysis_param_generation.py --mode oat

For each parameter sweep the following metrics are computed per output:
  - output_range     : max(Y) − min(Y) over the sweep (absolute)
  - pct_change       : output_range / |Y_baseline| × 100
  - direction        : sign of Spearman r between X values and Y values (+1 / −1)
  - signed_si        : direction × pct_change  (Signed Sensitivity Index)

Signed SI is the primary metric used in tornado plots and the heatmap.
A value of +50 means "varying this parameter over its full range increases
the output by 50 % relative to the all-default baseline."

─────────────────────────────────────────────────────────────
HOW TO USE:
  1. Set OAT_PARAMS_FILE  = path to oat_samples.xlsx
  2. Set OAT_OUTPUTS_FILE = path to your OAT model output CSV (row-matched)
  3. Run: python sensitivity_analysis_oat.py
─────────────────────────────────────────────────────────────
"""

import re
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import matplotlib.gridspec as gridspec
from scipy import stats
from pathlib import Path

# ─────────────────────────────────────────────
# CONFIGURATION
# ─────────────────────────────────────────────

OAT_PARAMS_FILE  = "oat_runs_manual_bounds.xlsx"
OAT_OUTPUTS_FILE = "../Sex_Trafficking_SDM/OATVariationOutput_manual.csv"

# Outputs to analyze (must exist as columns in your output CSV)
OUTPUTS_OF_INTEREST = [
    "TotalTSS",
    "pctTraffickedVsNontrafficked",
    "Buyers",
    "demand",
    "Traffickers",
    "pctSSCrimRecord",
]

# Human-readable display names for outputs
OUTPUT_LABELS = {
    "TotalTSS":                    "Total Sex Sellers",
    "pctTraffickedVsNontrafficked":"% Trafficked vs Non-Trafficked",
    "Buyers":                      "Buyers",
    "demand":                      "Demand",
    "Traffickers":                 "Traffickers",
    "pctSSCrimRecord":             "% with Criminal Record",
    "TotalSS":                     "Total Sex Sellers (All)",
    "TotalYouthTSS":               "Total Youth Sex Sellers",
    "TotalNonTraffickedSS":        "Non-Trafficked Sex Sellers",
    "EmServicesAdult":             "Adult Em Services",
    "STServicesAdult":             "Adult ST Services",
    "LTServicesAdult":             "Adult LT Services",
    "pctExitedCrimRecord":         "% Exited with Crim Record",
    "cost":                        "Cost",
}

# Max parameters shown in tornado plots and response curve grids; 0 = show all
TOP_N = 15

# Columns of the params file that are not model parameters
NON_PARAM_COLS = ["varied_param", "run_id", "scenario"]

# Directory where figures and CSVs are saved
OUTPUT_DIR = "sensitivity_results_oat"


# ─────────────────────────────────────────────
# DISPLAY HELPERS
# ─────────────────────────────────────────────

def _snake(name: str) -> str:
    """Convert camelCase / PascalCase to snake_case for display."""
    s = re.sub(r"([A-Z]+)([A-Z][a-z])", r"\1_\2", name)
    s = re.sub(r"([a-z\d])([A-Z])", r"\1_\2", s)
    return s.lower()


def _out_label(name: str) -> str:
    return OUTPUT_LABELS.get(name, name)


# ─────────────────────────────────────────────
# DATA LOADING
# ─────────────────────────────────────────────

def load_oat_data() -> tuple[pd.DataFrame, pd.DataFrame, pd.Series]:
    """
    Returns (params_df, outputs_df, varied_param_col).

    params_df       : all parameter columns (numeric), index = run_id
    outputs_df      : output columns limited to OUTPUTS_OF_INTEREST that exist
    varied_param_col: string Series identifying which parameter was swept each row
                      ('baseline' for the all-default row)
    """
    params_df  = pd.read_excel(OAT_PARAMS_FILE, sheet_name="oat_params", index_col=0)
    outputs_df = pd.read_csv(OAT_OUTPUTS_FILE, index_col=None)

    print(f"Loaded params : {OAT_PARAMS_FILE}  ({len(params_df)} rows)")
    print(f"Loaded outputs: {OAT_OUTPUTS_FILE}  ({len(outputs_df)} rows)")

    if len(params_df) != len(outputs_df):
        raise ValueError(
            f"Row mismatch: {len(params_df)} param rows vs {len(outputs_df)} output rows.\n"
            "Files must be row-aligned (same run order)."
        )

    if "varied_param" not in params_df.columns:
        raise ValueError(
            "'varied_param' column not found in params file. "
            "Make sure it was generated with --mode oat."
        )

    varied_param = params_df["varied_param"].reset_index(drop=True)
    params_df    = params_df.drop(columns="varied_param").reset_index(drop=True)
    outputs_df   = outputs_df.reset_index(drop=True)

    outputs_present = [c for c in OUTPUTS_OF_INTEREST if c in outputs_df.columns]
    missing = set(OUTPUTS_OF_INTEREST) - set(outputs_present)
    if missing:
        print(f"  WARNING: outputs not found in data: {sorted(missing)}")
    if not outputs_present:
        raise ValueError("No output columns found — check OUTPUTS_OF_INTEREST.")

    return params_df, outputs_df[outputs_present], varied_param


# ─────────────────────────────────────────────
# METRICS
# ─────────────────────────────────────────────

def compute_oat_metrics(
    params_df: pd.DataFrame,
    outputs_df: pd.DataFrame,
    varied_param: pd.Series,
) -> pd.DataFrame:
    """
    For every (parameter, output) pair compute OAT sensitivity metrics.

    Returns a DataFrame indexed by (parameter, output) with columns:
      output_range, pct_change, direction, signed_si,
      y_baseline, param_lo, param_hi, param_default
    """
    # Baseline row
    baseline_mask = varied_param == "baseline"
    if baseline_mask.sum() == 0:
        raise ValueError("No baseline row found (varied_param == 'baseline').")
    baseline_outputs = outputs_df[baseline_mask].iloc[0]
    baseline_params  = params_df[baseline_mask].iloc[0]

    sweep_params = [p for p in varied_param.unique() if p != "baseline"]

    records = []
    for param in sweep_params:
        mask = varied_param == param
        x_vals = params_df.loc[mask, param].values
        sort_idx = np.argsort(x_vals)
        x_sorted = x_vals[sort_idx]

        for out in outputs_df.columns:
            y_vals   = outputs_df.loc[mask, out].values
            y_sorted = y_vals[sort_idx]
            y_base   = baseline_outputs[out]

            out_range = float(y_sorted.max() - y_sorted.min())
            pct_change = (out_range / abs(y_base) * 100) if y_base != 0 else float("nan")

            direction = float(np.sign(stats.spearmanr(x_sorted, y_sorted).statistic))
            signed_si = direction * pct_change

            records.append({
                "parameter":    param,
                "output":       out,
                "output_range": out_range,
                "pct_change":   pct_change,
                "direction":    direction,
                "signed_si":    signed_si,
                "y_baseline":   y_base,
                "param_lo":     float(x_sorted.min()),
                "param_hi":     float(x_sorted.max()),
                "param_default": float(baseline_params.get(param, float("nan"))),
            })

    return pd.DataFrame(records).set_index(["parameter", "output"])


